/**
 * Enum representing HTTP methods used for routing.
 * Each enum value corresponds to a standard HTTP method (GET, POST, etc.).
 */
export enum Methods {
  /**
   * Represents the HTTP GET method.
   */
  Get = "get",

  /**
   * Represents the HTTP POST method.
   */
  Post = "post",

  /**
   * Represents the HTTP PATCH method.
   */
  Patch = "patch",

  /**
   * Represents the HTTP PUT method.
   */
  Put = "put",

  /**
   * Represents the HTTP DELETE method.
   */
  Delete = "delete",
}
